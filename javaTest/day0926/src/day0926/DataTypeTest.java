package day0926;

public class DataTypeTest {

	public static void main(String[] args) {
		char firstName; //하나의 문자를 저장하기 위한 변수를 선언
		firstName = '박'; //'박'이라는 문잘 firstName에 저장
		System.out.println(firstName);
		
		String name;  //문자열을 저장하기 위한 변수
		name = "박세연";
		System.out.println(name);		
	}

}
