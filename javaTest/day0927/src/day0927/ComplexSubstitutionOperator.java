package day0927;
//
public class ComplexSubstitutionOperator {
	public static void main(String[] args) {
		int sum = 0;
		int i=1;
		sum +=i; //sum= sum+i
		System.out.println("sum:"+sum); //1
		i++;
		sum +=i;
		System.out.println("sum:"+sum); //3
		i++;
		sum +=i;
		System.out.println("sum:"+sum); //6
		
	}

}
