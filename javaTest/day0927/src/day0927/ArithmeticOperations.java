package day0927;

public class ArithmeticOperations {

	public static void main(String[] args) {
		int a, b;
		a = 5;
		b = 2;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		System.out.println("--------------");
		System.out.println(5+2);
		System.out.println("5+2");
		System.out.println(5-2);
		System.out.println(5*2);
		System.out.println(5/2);
		System.out.println(5%2);
		

	}

}
